const APIFunction = async(inputVal) => {
    let baseURL = `${URL}/${fromCurrency.value}`;
    let response = await fetch(baseURL);
    let responseJson = await response.json();
    let data = responseJson.conversion_rates[toCurrency.value];
    console.log(data);
    messageFunction(data, inputVal);    
}


const messageFunction = (message, inputVal) => {
        const totalVal = Number(inputVal);
        finalMessage.innerText = `${totalVal}${fromCurrency.value} = ${totalVal * message}${toCurrency.value}`
        
}





